<?PHP

class calculator
{
    //We are expected to implement add, subtract, product and division

    public function add($a, $b)
    {
        return $a + $b;
    }

    public function subtract($a, $b)
    {
        return $a - $b;
    }

    public function multiply($a, $b)
    {
        return $a * $b;
    }

    public function divid($a, $b)
    {
        return $a / $b;
    }
}